public class iNCREMENTandDECREMENToPERATORS {

	public static void main(String[] args) {
		
		
//INCREMENT / DECREMENT OPERATORS ++ - -
		
	int a=10;
       //  a=a+1; will be 11
	  // a++; will also be 11 same comand shortcut
	 a++;
		
  System.out.println(a);	
		
		
	 int b=20;
	  b=b-1; // will be 19
	  b--;  // this will also be 19 just shortcut double minus
	        // will decreace the value by 1 
	  
	System.out.println(b);	
		
		// = is assignment operator that assigns value 

	}

}
