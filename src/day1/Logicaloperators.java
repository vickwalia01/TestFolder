package day1;

public class Logicaloperators {

	public static void main(String[] args) {
		
		// logical operators && ||  !
		// works between two boolean values
		
	boolean x=true;
	boolean y=false;
		
		System.out.println(x && y);  // false
		System.out.println(x || y);  // false
		System.out.println(!x);      // false
		System.out.println(!y);   // true
		
		
	}

}
